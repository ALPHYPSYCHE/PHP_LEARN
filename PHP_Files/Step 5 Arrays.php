<?php
//This script demonstrates both indexed and associative arrays.

// Indexed array
$fruits = array("Apple", "Banana", "Orange");

// Associative array
$info = array("name" => "Ali", "age" => 25, "city" => "New York");

// Output
echo $fruits[0]; // Outputs: Apple
echo $info["age"]; // Outputs: 25
?>
